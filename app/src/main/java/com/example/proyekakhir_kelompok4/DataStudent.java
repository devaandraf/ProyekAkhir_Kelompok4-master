package com.example.proyekakhir_kelompok4;

public class DataStudent {
    public String image;
    public String username;
    public String nim;
    public String prodi;
    public String fakultas;

    public DataStudent(String image, String username, String nim, String prodi, String fakultas){
        this.image = image;
        this.username = username;
        this.nim = nim;
        this.prodi = prodi;
        this.fakultas = fakultas;
    }
}
